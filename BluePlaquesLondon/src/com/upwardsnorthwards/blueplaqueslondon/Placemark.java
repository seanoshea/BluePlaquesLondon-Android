package com.upwardsnorthwards.blueplaqueslondon;

public class Placemark {

	private String featureDescription;
	private String name;
	private String title;
	private String styleUrl;
	private double latitude;
	private double longitude;
	private int placemarkPinType;

	public String key() {
		return Double.toString(latitude) + Double.toString(longitude);
	}

	public String getFeatureDescription() {
		return featureDescription;
	}

	public void setFeatureDescription(String featureDescription) {
		this.featureDescription = featureDescription;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getStyleUrl() {
		return styleUrl;
	}

	public void setStyleUrl(String styleUrl) {
		this.styleUrl = styleUrl;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public int getPlacemarkPinType() {
		return placemarkPinType;
	}

	public void setPlacemarkPinType(int placemarkPinType) {
		this.placemarkPinType = placemarkPinType;
	}
}
